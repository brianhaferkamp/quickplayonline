$(".game-reset button").on("click", function () {
  // reset scoreboard
  $(".v-score input").val("");
  $(".h-score input").val("");
  $(".v-runs").text("0");
  $(".h-runs").text("0");

  changeScoreboard();

  // reset pitcher names
  $(".v-staff .name").css({
    "font-weight": "400"
  });

  $(".v-lineup .name").css({
    "font-weight": "400"
  });

  $(".h-staff .name").css({
    "font-weight": "400"
  });

  $(".h-lineup .name").css({
    "font-weight": "400"
  });

  // reset pitching grades
  $(".v-pitcher-rating li").removeClass("active");
  $(".v-pitcher-rating li").first().addClass("active");
  $(".h-pitcher-rating li").removeClass("active");
  $(".h-pitcher-rating li").first().addClass("active");

  // reset cards
  $(".card ul li").removeClass("show");
  $(".card").removeClass("no-bg");
  $(".situation-cards li").first().addClass("show");
});

