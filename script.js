$(".hits-cards").on("click", function () {
  var $this = $(this);
  var cardSuitPlaceholder = $this.find(".card").find(".card-suit");

  $(".hits-cards .card").hide();

  // random roll

  var result;

  var sides = 4;

  if (
    $(".v-lineup .current").text() === "Pitcher" ||
    $(".h-lineup .current").text() === "Pitcher"
  ) {
    sides = 2;
    console.log("pitcher batting = " + sides);
  }

  console.log(sides);

  // roll the dice
  function rollHitsDice() {
    var dice = {
      sides: sides,
      roll: function () {
        var randomNumber = Math.floor(Math.random() * this.sides) + 1;
        return randomNumber;
      }
    };

    result = dice.roll();
  }

  rollHitsDice();

  // show new result

  // show random situation card
  if (result == 1) {
    result = "jack";
  } else if (result == 2) {
    result = "queen";
  } else if (result == 3) {
    result = "king";
  } else if (result == 4) {
    result = "ace";
  }

  // choose suit
  var suit;
  cardSuit(cardSuitPlaceholder);

  $(".hits-card-" + result).addClass("show");
  $this.find(".card").addClass("no-bg");

  $(".hits-cards .card").fadeIn();

  // remove .current from pitcher
  // prevents situation where a pitcher is selected while a hitter on the other team is at bat, causing the batter to use 2 sides
  if ($(".v-lineup .current").text() === "Pitcher") {
    $(".v-lineup .current").removeClass("current");
  } else if ($(".h-lineup .current").text() === "Pitcher") {
    $(".h-lineup .current").removeClass("current");
  }
});

